package com.example.cadin.model;

public class Triaje {
    String idtri;
    String fchtri;
    String edad;
    String peso;
    String talla;

    public String getIdtri() {
        return idtri;
    }

    public void setIdtri(String idtri) {
        this.idtri = idtri;
    }

    public String getFchtri() {
        return fchtri;
    }

    public void setFchtri(String fchtri) {
        this.fchtri = fchtri;
    }

    public String getEdad() {
        return edad;
    }

    public void setEdad(String edad) {
        this.edad = edad;
    }

    public String getPeso() {
        return peso;
    }

    public void setPeso(String peso) {
        this.peso = peso;
    }

    public String getTalla() {
        return talla;
    }

    public void setTalla(String talla) {
        this.talla = talla;
    }
}
