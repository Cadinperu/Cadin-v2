package com.example.cadin.model;

public class Vacunas {
    private int id;
    private int idcli;
    private int idvac;
    private String cod;
    private String nom;
    private String cantdos;
    private String fecha;
    private String motiv;
    private int idlic;
    private int idtri;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdcli() {
        return idcli;
    }

    public void setIdcli(int idcli) {
        this.idcli = idcli;
    }

    public int getIdvac() {
        return idvac;
    }

    public void setIdvac(int idvac) {
        this.idvac = idvac;
    }

    public String getCod() {
        return cod;
    }

    public void setCod(String cod) {
        this.cod = cod;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getCantdos() {
        return cantdos;
    }

    public void setCantdos(String cantdos) {
        this.cantdos = cantdos;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getMotiv() {
        return motiv;
    }

    public void setMotiv(String motiv) {
        this.motiv = motiv;
    }

    public int getIdlic() {
        return idlic;
    }

    public void setIdlic(int idlic) {
        this.idlic = idlic;
    }

    public int getIdtri() {
        return idtri;
    }

    public void setIdtri(int idtri) {
        this.idtri = idtri;
    }
}
