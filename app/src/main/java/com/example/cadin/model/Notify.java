package com.example.cadin.model;

import android.widget.ImageView;

public class Notify {
    private String txtitem;
    private String statitem;
    private ImageView iconitem;

    public String getTxtitem() {
        return txtitem;
    }

    public void setTxtitem(String txtitem) {
        this.txtitem = txtitem;
    }

    public ImageView getIconitem() {
        return iconitem;
    }

    public void setIconitem(ImageView iconitem) {
        this.iconitem = iconitem;
    }

    public String getStatitem() {
        return statitem;
    }

    public void setStatitem(String statitem) {
        this.statitem = statitem;
    }
}
